    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        try {
            String calculosValores = "";
            Registry reg = LocateRegistry.getRegistry("localhost", 8080);
            FibonacciInterface fibo = (FibonacciInterface)reg.lookup("fibonacci");
            
            long[] arr = fibo.fibonacci(Integer.valueOf(n.getText()));
     
            for (int i = 0; i < arr.length; i++) {
                calculosValores += Long.toString(arr[i])+"\n";
            }
            
            calculos.setText(calculosValores);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_calcularActionPerformed

